SCENE_FIRST = '''
1.情感抒发:
    说明： 内容的核心目的是表达或唤起某种特定的、强烈的情绪或感受。内容可能包含个人内心独白、对事件的反应、抒情画面，或旨在引发观众共鸣（如快乐、悲伤、愤怒、爱、思念、励志、迷茫等）。
    关键特征： 重在情绪的传递与共鸣，而非单纯记录事件或展示事物本身。
    典型内容： 表达爱意、失恋倾诉、励志故事、思乡之情、人生感悟、爱国情怀、心灵鸡汤等。

2.自我展示:
    说明： 内容的核心焦点是创作者本人，目的是展示其个人形象、才艺、技能、观点、个性魅力或生活方式。重点在于“我”是谁，“我”能做什么，“我”的想法是什么。
    关键特征： 创作者是绝对主角，内容围绕其自身展开。
    典型内容： 才艺表演（唱歌、跳舞、乐器）、技能展示（绘画、手工、烹饪）、观点表达/评论、个人Vlog（以展示自我为主）、穿搭/妆容展示、挑战类内容（主要展示个人能力）。

3.生活记录:
    说明： 内容的主要目的是真实、客观地记录日常生活片段、经历或所见的事物。侧重于“发生了什么”或“这是什么”，而非刻意表演或强烈的情感输出。
    关键特征： 真实性、日常性、记录性，主角可以是人、宠物、事物或环境。
    典型内容： 日常Vlog片段（吃饭、通勤、购物）、宠物日常活动、风景随拍、食物记录、交通工具使用、随手拍的零碎片段。

4.搞笑:
    说明： 内容的核心且唯一目的是制造欢乐，引发观众的笑声。内容可以是幽默表演、滑稽模仿、意外状况、有趣对话、喜剧情节、搞笑挑战或纯粹的娱乐段子。
    关键特征： 娱乐性、幽默感，目标是逗乐观众。
    典型内容： 搞笑短剧、脱口秀片段、模仿秀、失误集锦、宠物搞笑瞬间、朋友间的幽默互动、网络流行梗的演绎。

5.社媒的:
    说明： 内容、形式或目标高度依赖或紧密围绕社交媒体平台的特性和生态。这包括讨论平台上的热点（影视剧、名人、运动）、参与平台流行挑战/话题，或者内容本身就是关于社交媒体现象、网红、网络文化的。
    关键特征： 内容与社交媒体平台上的流行元素、话题或文化强相关。
    典型内容： 影视剧解说/混剪、明星八卦/集锦、体育赛事精彩瞬间/讨论、参与热门挑战（如踢瓶盖挑战）、讨论网红事件、分析社交媒体趋势。

6.节日:
    说明： 内容明确围绕公共性、传统性或广泛认知与庆祝的特定文化/宗教节日展开。主题、氛围、装饰、活动或祝福语都与该节日直接相关。
    关键特征： 与大众熟知的、日历上固定的节日紧密关联。
    典型内容： 春节贴春联/拜年、圣诞节装饰圣诞树/送礼物、中秋节赏月/吃月饼、国庆节庆祝活动、万圣节装扮、开斋节祝福等。

7.纪念日:
    说明： 内容围绕对个人、特定关系或群体具有独特意义的日期或事件展开。这个日期标志着个人生命历程、关系里程碑或特定时刻，通常具有私人性或小范围性（不同于大众节日）。
    关键特征： 强调个人化、有意义的时刻或周期性祝福（如早安晚安）。
    典型内容： 生日庆祝、结婚纪念、恋爱周年、毕业典礼、季节变换的记录/感怀、日常祝福（早安/晚安/周末愉快）、特定仪式（毕业典礼、成人礼）。

8.特殊的:
    说明： 内容属于非常规、非典型的类别，主要涵盖商业推广目的或纯口播形式的内容。这些内容通常不符合上述其他7个以用户生成内容（UGC）或日常生活/情感为核心的分类。
    关键特征： 商业属性明显或形式为纯人声讲述。
    典型内容： 商品广告/宣传片、直播带货片段、品牌营销活动内容、纯口播的知识分享、观点评论（无画面辅助的讲述）。
'''


def get_theme_prompt(input_format):
    prompt = f'''
        ## 角色
        你是一个有用的标签打标助手，善于分析理解视频或图片的内容，然后按照要求对其进行打标；

        ## 要求
            1.整体理解视频或图片的内容，然后开始标签打标，输出的标签从下面的标签中选择，标签位于<label>中间，标签后面有一些描述说明，用于辅助你进行打标：
                <label>{SCENE_FIRST}<label>；
            2.结果输出最符合的1~2个标签，并且输出每个标签的置信度,置信度在0~1之间，总置信度和为1；
            3.输出格式用json格式进行输出，对于每一个标签，包含字段如下：
                - 标签：对应的标签；
                - 置信度：对应标签的置信度；
                - 说明：输出这个标签的原因；

        ## 输出实例：
            - 示例1：
                标签： xxx；
                置信度：0.9；

        现在，请按照上面的要求分析给出的{input_format},然后进行格式化输出，不要输出解释或者其它无关内容。
    '''

    return prompt

